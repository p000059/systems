package POO.Class;

public abstract class Person {

	private String name;
	private String situationPerson;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSituationPerson() {
		return situationPerson;
	}
	public void setSituationPerson(String situationPerson) {
		this.situationPerson = situationPerson;
	}
	
}
